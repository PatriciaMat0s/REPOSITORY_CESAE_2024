package EX01_redo;

public class Musica {
    private String titulo;
    private String genero;
    private String artista;
    private int segundos;

    public Musica(String titulo, String genero, String artista, int segundos) {
        this.titulo = titulo;
        this.genero = genero;
        this.artista = artista;
        this.segundos = segundos;
    }

}
